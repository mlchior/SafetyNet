package com.openclassrooms.safetynet.model;


public class Person {


  private String firstName;
    private String lastName;
    private String adress;
    private String city;
    private int zip;
    private String phone;
    private String email;

    public Person(String firstName, String lastName, String adress, String city, int zip, String phone, String email) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.adress = adress;
        this.city = city;
        this.zip = zip;
        this.phone = phone;
        this.email = email;
    }

}





